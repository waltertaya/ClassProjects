import numpy as np

np1 = np.array([1, 2, 3])
print(np1)
# length of array
print(np1.shape)
# array range
np2 = np.arange(0, 30, 2)
print(np2)
# printing array of zeros
np3 = np.zeros((3, 4))
print(np3)
# printing array of ones
np4 = np.ones((3, 4))
print(np4)
# printing array of random numbers
np5 = np.random.random((3, 4))
print(np5)
# printing full array
np6 = np.full((3, 4), 7)
print(np6)
# converting list to array
np7 = np.array([1, 2, 3])
